import math
print('The value of 3 to the power 2 is: ', math.pow(3, 2))
print('The value of 5 factorial is: ', math.factorial(5))
print('The value of square root of 25 is: ', math.sqrt(25))
print('The celing value of 2.3 is: ', math.ceil(2.3))
print('The floor value of 2.3 is: ', math.floor(2.3))
print('GCD of 5 and 15 is: ', math.gcd(5, 15))
print('The value of log 2 to the base 3 is: ', math.log(2, 3))
print('The value of log 10000 to the base 10 is: ', math.log10(1000))
print('The value of constant pi is: ', math.pi)
print('The value of constant e is: ', math.e)
